	// Instruction Size Calculation
	// ========================================================================

	/// <summary>
	/// Calculates the size of an instruction in bytes.
	/// </summary>
	private int GetInstructionSize(InstructionNode node, AddressingMode effectiveMode) {
		// Branch instructions are always 2 bytes (opcode + relative offset)
		var mnemonic = node.Mnemonic;
		if (mnemonic.Length > 2 && mnemonic[^2] == '.') {
			mnemonic = mnemonic[..^2];
		}

		// V30MZ-specific instruction sizing
		if (Target == TargetArchitecture.V30MZ) {
			var v30Size = GetV30MZInstructionSize(node, mnemonic);
			if (v30Size > 0) {
				return v30Size;
			}
		}

		if (IsBranchInstruction(mnemonic)) {
			return 2; // opcode + 1 byte relative offset
		}

		// Base opcode is 1 byte
		int size = 1;

		// Add operand size based on effective addressing mode
		size += GetOperandSize(effectiveMode, node.SizeSuffix, mnemonic);

		return size;
	}

	/// <summary>
	/// Tries to get a constant value from an operand expression without side effects.
	/// Only returns a value for numeric literals and simple constant expressions
	/// (EQU-defined constants). Does not resolve labels or anonymous references.
	/// </summary>
	private long? TryGetConstantOperandValue(ExpressionNode operand) {
		if (operand is NumberLiteralNode num) {
			return num.Value;
		}

		// Resolve EQU-defined constants (not address labels)
		if (operand is IdentifierNode ident &&
			SymbolTable.TryGetSymbol(ident.Name, out var symbol) &&
			symbol is not null && symbol.IsDefined &&
			symbol.Type == SymbolType.Constant && symbol.Value.HasValue) {
			return symbol.Value.Value;
		}

		return null;
	}

	/// <summary>
	/// Resolves the effective addressing mode for instruction sizing, mirroring
	/// the code generator's Absolute→ZeroPage optimization so label addresses
	/// are consistent between analyzer and code generator.
	/// </summary>
	private AddressingMode ResolveEffectiveSizingMode(string mnemonic, AddressingMode mode, long value) {
		// Strip size suffix for instruction lookup
		var lower = mnemonic.ToLowerInvariant();
		if (lower.Length > 2 && lower[^2] == '.') {
			lower = lower[..^2];
		}

		// Branch instructions: Absolute → Relative (already handled by IsBranchInstruction)
		if (IsBranchInstruction(lower)) {
			return mode;
		}

		var isZeroPage = value >= 0 && value <= 0xff;
		if (!isZeroPage) {
			return mode;
		}

		return mode switch {
			AddressingMode.Absolute when SupportsAddressingMode(lower, AddressingMode.ZeroPage) => AddressingMode.ZeroPage,
			AddressingMode.AbsoluteX when SupportsAddressingMode(lower, AddressingMode.ZeroPageX) => AddressingMode.ZeroPageX,
			AddressingMode.AbsoluteY when SupportsAddressingMode(lower, AddressingMode.ZeroPageY) => AddressingMode.ZeroPageY,
			AddressingMode.Indirect when SupportsAddressingMode(lower, AddressingMode.ZeroPageIndirect) => AddressingMode.ZeroPageIndirect,
			_ => mode
		};
	}

	/// <summary>
	/// Checks whether the current target architecture supports a given addressing mode
	/// for the specified instruction mnemonic.
	/// </summary>
	private bool SupportsAddressingMode(string mnemonic, AddressingMode mode) {
		var profile = Arch.TargetResolver.TryGetProfile(Target);
		if (profile is null) return false;
		return profile.Encoder.TryEncode(mnemonic, mode, out _);
	}

	/// <summary>
	/// Gets the instruction size for V30MZ-specific instructions.
	/// Returns 0 if the instruction is not V30MZ-specific (fall through to generic sizing).
	/// </summary>
	private int GetV30MZInstructionSize(InstructionNode node, string mnemonic) {
		var lower = mnemonic.ToLowerInvariant();

		// Conditional jumps: opcode + rel8 = 2 bytes
		if (InstructionSetV30MZ.TryGetConditionalJump(lower, out _)) {
			return 2;
		}

		// Loop instructions: opcode + rel8 = 2 bytes
		if (InstructionSetV30MZ.TryGetLoopInstruction(lower, out _)) {
			return 2;
		}

		// Check if operand is a register identifier
		bool operandIsRegister = node.Operand is IdentifierNode id && InstructionSetV30MZ.IsRegister(id.Name);
		bool operandIsSegment = node.Operand is IdentifierNode sid && InstructionSetV30MZ.IsSegmentRegister(sid.Name);

		// Multi-byte implied: AAM ($d4 $0a), AAD ($d5 $0a) = 2 bytes
		if (lower is "aam" or "aad") {
			return 2;
		}

		// Register-operand instructions: single opcode byte
		if (operandIsRegister || operandIsSegment) {
			// push/pop/inc/dec/xchg reg16, push/pop segment = 1 byte
			if (lower is "push" or "pop" or "inc" or "dec" or "xchg") {
				return 1;
			}
		}

		// INT n = 2 bytes (opcode + imm8)
		if (lower == "int") {
			return 2;
		}

		// Near JMP = 3 bytes (opcode + rel16), near CALL = 3 bytes (opcode + rel16)
		if (lower is "jmp" or "call") {
			return 3;
		}

		// RET/RETF with operand = 3 bytes (opcode + imm16)
		if (lower is "ret" or "retf" && node.Operand != null) {
			return 3;
		}

		// PUSH immediate: 2 bytes (push imm8 $6a) or 3 bytes (push imm16 $68)
		if (lower == "push" && node.Operand != null && !operandIsRegister && !operandIsSegment) {
			if (node.SizeSuffix == 'b') {
				return 2;
			}
			return 3;
		}

		// Fall through to generic sizing
		return 0;
	}

	/// <summary>
	/// Gets the operand size for an addressing mode.
	/// </summary>
	private int GetOperandSize(AddressingMode mode, char? sizeSuffix, string? mnemonic = null) {
		// Size suffix overrides
		if (sizeSuffix.HasValue) {
			return sizeSuffix.Value switch {
				'b' => 1,
				'w' => 2,
				'l' => 3,
				_ => 0
			};
		}

		// For 65816 immediate mode, size depends on M/X flags and instruction
		if (Target == TargetArchitecture.WDC65816 && mode == AddressingMode.Immediate && mnemonic != null) {
			var lower = mnemonic.ToLowerInvariant();
			// Index register instructions use X flag
			if (lower is "ldx" or "ldy" or "cpx" or "cpy") {
				return _indexIs16Bit ? 2 : 1; // 2 bytes in 16-bit, 1 byte in 8-bit
			}
			// Accumulator instructions use M flag
			if (lower is "lda" or "adc" or "sbc" or "cmp" or "and" or "ora" or "eor" or "bit") {
				return _accumulatorIs16Bit ? 2 : 1; // 2 bytes in 16-bit, 1 byte in 8-bit
			}
			// REP/SEP are always 8-bit immediate
			if (lower is "rep" or "sep") {
				return 1;
			}
			// PEA is always 16-bit
			if (lower is "pea") {
				return 2;
			}
			// Default to current accumulator size for other immediate instructions
			return _accumulatorIs16Bit ? 2 : 1;
		}

		return mode switch {
			AddressingMode.Implied => 0,
			AddressingMode.Accumulator => 0,
			AddressingMode.Immediate => 1, // 6502/SM83: always 1 byte immediate
			AddressingMode.ZeroPage => 1,
			AddressingMode.ZeroPageX => 1,
			AddressingMode.ZeroPageY => 1,
			AddressingMode.Absolute => 2,
			AddressingMode.AbsoluteX => 2,
			AddressingMode.AbsoluteY => 2,
			AddressingMode.Indirect => 2,
			AddressingMode.IndexedIndirect => 1,
			AddressingMode.IndirectIndexed => 1,
			AddressingMode.Relative => 1,
			AddressingMode.AbsoluteLong => 3,
			AddressingMode.AbsoluteLongX => 3,
			AddressingMode.StackRelative => 1,
			AddressingMode.StackRelativeIndirectIndexed => 1,
			AddressingMode.DirectPageIndirectLong => 1,
			AddressingMode.DirectPageIndirectLongY => 1,
			AddressingMode.AbsoluteIndirectLong => 2,
			AddressingMode.AbsoluteIndexedIndirect => 2,
			AddressingMode.BlockMove => 2,
			AddressingMode.MemoryReference => 0, // GB-specific, varies
			_ => 0
		};
	}
}

