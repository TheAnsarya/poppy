	/// <summary>
	/// Handles .target directive with architecture argument.
	/// </summary>
	private void HandleTargetDirective(DirectiveNode node) {
		if (_pass != 1) return;

		if (node.Arguments.Count < 1) {
			_errors.Add(new SemanticError(
				".target directive requires an architecture (nes, snes, gb, atari2600, lynx, genesis, sms, ws, gba, spc700, tg16, channelf)",
				node.Location));
			return;
		}

		// Accept both identifiers (e.g., "nes") and numeric literals (e.g., 6502, 65816)
		// since common CPU names like 6502, 6507, 2600, 65816, 68000 are registered aliases
		string targetName;
		if (node.Arguments[0] is IdentifierNode targetNode) {
			targetName = targetNode.Name;
		} else if (node.Arguments[0] is NumberLiteralNode numNode) {
			targetName = numNode.Value.ToString();
		} else {
			_errors.Add(new SemanticError(
				".target directive requires an identifier or numeric CPU name (e.g., nes, 6502, 65816)",
				node.Location));
			return;
		}

		var target = Arch.TargetResolver.Resolve(targetName);

		if (target is null) {
			_errors.Add(new SemanticError(
				$"Unknown target architecture: {targetName}",
				node.Location));
			return;
		}

		SetTarget(target.Value, node);
	}

	/// <summary>
	/// Sets the target architecture.
	/// </summary>
	private void SetTarget(TargetArchitecture target, DirectiveNode node) {
		// Allow setting to the same value (idempotent)
		if (_targetSetFromSource && Target != target) {
			_errors.Add(new SemanticError(
				"Target architecture already set - cannot change",
				node.Location));
			return;
		}

		Target = target;
		_targetSetFromSource = true;
	}

