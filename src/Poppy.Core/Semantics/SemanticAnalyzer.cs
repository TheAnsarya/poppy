				break;
			case "i16":
				_indexIs16Bit = true;
				break;
			case "smart":
				// Enable automatic M/X tracking from REP/SEP
				break;

			// NES mapper
			case "mapper":
			// iNES header directives
			case "ines_prg":
			case "ines_chr":
			case "ines_mapper":
			case "ines_submapper":
			case "ines_mirroring":
			case "ines_battery":
			case "ines_trainer":
			case "ines_fourscreen":
			case "ines_prgram":
			case "ines_chrram":
			case "ines_pal":
			case "ines2":
				if (_profile?.TryHandleDirective(node, this) != true) {
					AddError($".{node.Name.ToLowerInvariant()} directive is only valid for NES/6502 target", node.Location);
				}
				break;

			// SNES header directives
			case "snes_title":
			case "snes_region":
			case "snes_version":
			case "snes_rom_size":
			case "snes_ram_size":
			case "snes_fastrom":
				if (_profile?.TryHandleDirective(node, this) != true) {
					AddError($".{node.Name.ToLowerInvariant()} directive is only valid for SNES/65816 target", node.Location);
				}
				break;

			// Game Boy header directives
			case "gb_title":
			case "gb_cgb":
			case "gb_sgb":
			case "gb_cartridge_type":
			case "gb_rom_size":
			case "gb_ram_size":
			case "gb_region":
			case "gb_version":
				if (_profile?.TryHandleDirective(node, this) != true) {
					AddError($".{node.Name.ToLowerInvariant()} directive is only valid for Game Boy/SM83 target", node.Location);
				}
				break;

			// Atari Lynx header/boot directives
			case "lynx_name":
			case "lynx_manufacturer":
			case "lynx_rotation":
			case "lynx_bank0_size":
			case "lynx_bank1_size":
			case "lynxentry":
