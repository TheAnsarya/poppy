				if (_profile?.TryHandleDirective(node, this) != true) {
					AddError($".{node.Name.ToLowerInvariant()} directive is only valid for Atari Lynx/65SC02 target", node.Location);
				}
				break;

			// GBA header directives
			case "gba_title":
			case "gba_game_code":
			case "gba_maker_code":
			case "gba_version":
			case "gba_entry":
				if (_profile?.TryHandleDirective(node, this) != true) {
					AddError($".{node.Name.ToLowerInvariant()} directive is only valid for GBA/ARM7TDMI target", node.Location);
				}
				break;

			// SPC700 file directives
			case "spc_song_title":
			case "spc_game_title":
			case "spc_artist":
			case "spc_entry":
				if (_profile?.TryHandleDirective(node, this) != true) {
					AddError($".{node.Name.ToLowerInvariant()} directive is only valid for SPC700 target", node.Location);
				}
				break;

			// Assertions and diagnostics
			case "assert":
				HandleAssertDirective(node);
				break;

			case "error":
				HandleErrorDirective(node);
				break;

			case "warning":
				HandleWarningDirective(node);
				break;

			case "bank":
				HandleBankDirective(node);
				break;

			case "banksize":
				HandleBanksizeDirective(node);
				break;

			default:
				// Visit arguments for symbol resolution
				foreach (var arg in node.Arguments) {
					arg.Accept(this);
				}

				break;
		}

		return null;
	}

	/// <inheritdoc />
	public object? VisitExpression(ExpressionNode node) {
		// Base expression - should not be called directly
		return null;
